    private int[] obtenerPosicionBoton(JButton boton) {
        String[] posicion = boton.getName().split("I");

        int[] convierte = new int[2];
        convierte[0] = Integer.parseInt(posicion[0]);
        convierte[1] = Integer.parseInt(posicion[1]);
        System.out.println("X: " + convierte[0]);
        System.out.println("Y: " + convierte[1]);
        return convierte;
    }

